bot.command('unlink', async (ctx: Context) => {
  const chatId = ctx.chat!.id.toString();
  // Revoke all web sessions for the owning user too, otherwise a long-lived JWT
  // can still be used by any code that picks the latest session for this user.
  const user = await prisma.user.findFirst({ where: { telegramChatId: chatId }, select: { id: true } });
  const updated = await prisma.user.updateMany({
    where: { telegramChatId: chatId },
    data: { telegramChatId: null, telegramLinked: false },
  });
  if (user) {
    await prisma.session.deleteMany({ where: { userId: user.id } });
  }
  if (updated.count > 0) {
    await ctx.reply('✅ Telegram unlinked and all web sessions revoked. Re-login in the app if you want to continue.');
  } else {
    await ctx.reply('❌ No wallet was linked to this account.');
  }
});

