// --- Approve pending transfer (Guardian approval flow) ---
bot.action(/^approve_tx:(.+)$/, async (ctx) => {
  const txId = ctx.match[1];
  try {
    const tx = await prisma.agentTransaction.findUnique({ where: { id: txId }, select: { userId: true, status: true, amount: true, tokenIn: true, toAddress: true } });
    if (!tx) { await ctx.answerCbQuery('Transaction not found'); return; }
    if (tx.status !== 'PENDING_APPROVAL') { await ctx.answerCbQuery('Already processed'); return; }
    const owner = await prisma.user.findUnique({ where: { id: tx.userId }, select: { telegramChatId: true } });
    const pressedBy = ctx.chat?.id?.toString();
    if (!owner?.telegramChatId || !pressedBy || owner.telegramChatId !== pressedBy) {
      await ctx.answerCbQuery('Not authorized');
      return;
    }
    await ctx.answerCbQuery('⏳ Executing...');
    await ctx.editMessageReplyMarkup(undefined);
    await ctx.reply(`⏳ <b>Executing transfer...</b>\n\nSending <b>${tx.amount} ${tx.tokenIn}</b>...`, { parse_mode: 'HTML' });
    const result = await executePendingTx(txId);
    if (result.success) {
      const explorerBase = 'https://testnet.arcscan.app';
      const toShort = result.toAddress ? `${result.toAddress.slice(0, 6)}...${result.toAddress.slice(-4)}` : '';
      await ctx.reply(
        `✅ <b>Transfer complete!</b>\n\n` +
        `📤 Sent: <b>${result.amount}</b>\n` +
        `📍 To: <code>${toShort}</code>\n` +
        (result.txHash ? `🔗 <a href="${explorerBase}/tx/${result.txHash}">View on Arcscan</a>` : ''),
        { parse_mode: 'HTML' }
      );
    } else {
      await ctx.reply(`❌ Transfer failed: ${result.error}`, { parse_mode: 'HTML' });
    }
  } catch (err) {
    console.warn('[bot] approve_tx handler failed for tx', txId, err);
    await ctx.answerCbQuery('Error');
  }
});

// --- Reject pending transfer ---
bot.action(/^reject_tx:(.+)$/, async (ctx) => {
  const txId = ctx.match[1];
  try {
    const tx = await prisma.agentTransaction.findUnique({ where: { id: txId }, select: { userId: true, status: true } });
    if (!tx) { await ctx.answerCbQuery('Transaction not found'); return; }
    if (tx.status !== 'PENDING_APPROVAL') { await ctx.answerCbQuery('Already processed'); return; }
    const owner = await prisma.user.findUnique({ where: { id: tx.userId }, select: { telegramChatId: true } });
    const pressedBy = ctx.chat?.id?.toString();
    if (!owner?.telegramChatId || !pressedBy || owner.telegramChatId !== pressedBy) {
      await ctx.answerCbQuery('Not authorized');
      return;
    }
    await ctx.answerCbQuery('❌ Rejected');
    await ctx.editMessageReplyMarkup(undefined);
    await rejectPendingTx(txId);
    await ctx.reply('❌ Transfer rejected and cancelled.');
  } catch (err) {
    console.warn('[bot] reject_tx handler failed for tx', txId, err);
    await ctx.answerCbQuery('Error');
  }
});

// --- Auto-execute handler (manual button press) ---
bot.action(/^auto_exec:(.+)$/, async (ctx) => {
  const alertId = ctx.match[1];
  try {
    const alert = await prisma.alert.findUnique({ where: { id: alertId }, select: { userId: true, status: true } });
    if (!alert || alert.status === 'ACKNOWLEDGED') {
      await ctx.answerCbQuery('Already processed');
      return;
    }
    // Ownership check, prevent IDOR via crafted callback_data from another user's client.
    const owner = await prisma.user.findUnique({ where: { id: alert.userId }, select: { telegramChatId: true } });
    const pressedBy = ctx.chat?.id?.toString();
    if (!owner?.telegramChatId || !pressedBy || owner.telegramChatId !== pressedBy) {
      await ctx.answerCbQuery('Not authorized');
      return;
    }
    await ctx.answerCbQuery('⚡ Executing...');
    await ctx.editMessageReplyMarkup(undefined);
    await ctx.reply(`⚡ <b>Executing protective swap...</b>\n🤖 Agent is processing...`, { parse_mode: 'HTML' });

    const swapResult = await executeSwapForAlert(alertId);

    await prisma.alert.update({
      where: { id: alertId },
      data: { status: 'ACKNOWLEDGED', executedAt: new Date() },
    });

    if (swapResult.success) {
      const explorerBase = 'https://testnet.arcscan.app';
      await ctx.reply(
        `✅ <b>Swap executed!</b>\n\n` +
        `📤 Sold: <b>${swapResult.fromAmount}</b>\n` +
        `📥 Received: <b>${swapResult.toAmount}</b>\n` +
        `🌐 Network: Arc Testnet\n` +
        `🔗 <a href="${explorerBase}/tx/${swapResult.txHash}">View on Arcscan</a>`,
        { parse_mode: 'HTML' }
      );
    } else {
      await ctx.reply(`⚠️ Swap failed: ${swapResult.error}`, { parse_mode: 'HTML' });
    }
  } catch (err) {
    console.warn('[bot] auto_exec handler failed for alert', alertId, err);
    await ctx.answerCbQuery('Error');
  }
});

