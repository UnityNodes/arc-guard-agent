bot.command('link', async (ctx: Context) => {
  const chatId = ctx.chat!.id.toString();
  const args = (ctx.message as { text: string }).text.split(' ').slice(1);
  const code = args[0]?.trim();

  if (!code) {
    await ctx.reply('❌ Usage: `/link <code>`\n\nGet your link code from the GuardAgent app.', { parse_mode: 'Markdown' });
    return;
  }

  try {
    const walletAddress = await redis.get(`link:${code}`);
    if (!walletAddress) {
      await ctx.reply('❌ Invalid or expired code. Please generate a new one from the app.');
      return;
    }

    // Block silent takeover: if this wallet is already linked to a different chatId,
    // require the user to explicitly /unlink from the existing chat first.
    const existing = await prisma.user.findUnique({
      where: { walletAddress },
      select: { telegramChatId: true },
    });
    if (existing?.telegramChatId && existing.telegramChatId !== chatId) {
      await ctx.reply(
        '❌ This wallet is already linked to a different Telegram account.\n' +
        'Run /unlink from the existing Telegram account first, or regenerate a link code in the app.'
      );
      return;
    }

    await prisma.user.update({
      where: { walletAddress },
      data: { telegramChatId: chatId, telegramLinked: true },
    });
    await redis.del(`link:${code}`);

    await ctx.reply(
      `✅ *Wallet linked successfully!*\n\n` +
      `Wallet: \`${walletAddress.slice(0, 6)}...${walletAddress.slice(-4)}\`\n\n` +
      `You'll now receive instant price alerts here. Use /rules to see your monitoring rules.`,
      { parse_mode: 'Markdown' }
    );
  } catch (err) {
    console.error('Link error:', err);
    await ctx.reply('❌ Something went wrong. Please try again.');
  }
});

bot.command('status', async (ctx: Context) => {
  const chatId = ctx.chat!.id.toString();
  const user = await prisma.user.findFirst({
    where: { telegramChatId: chatId },
    include: {
      rules: { where: { isActive: true } },
      _count: { select: { alerts: true } },
    },
  });

  if (!user) {
    await ctx.reply('❌ Wallet not linked. Use `/link <code>` from the app.', { parse_mode: 'Markdown' });
    return;
  }

  const paused = !user.isActive;
  await ctx.reply(
    `${paused ? '⏸️' : '✅'} *GuardAgent Status*\n\n` +
    `👛 Wallet: \`${user.walletAddress.slice(0, 6)}...${user.walletAddress.slice(-4)}\`\n` +
    `📋 Active rules: ${user.rules.length}\n` +
    `🔔 Total alerts: ${user._count.alerts}\n` +
    `💎 Plan: ${user.plan}\n` +
    `📡 Monitoring: ${paused ? '*PAUSED*' : '*ACTIVE*'}`,
    { parse_mode: 'Markdown' }
  );
});

bot.command('rules', async (ctx: Context) => {
  const chatId = ctx.chat!.id.toString();
  const user = await prisma.user.findFirst({
    where: { telegramChatId: chatId },
    include: { rules: { where: { isActive: true } } },
  });

  if (!user) {
    await ctx.reply('❌ Wallet not linked. Use `/link <code>` from the app.', { parse_mode: 'Markdown' });
    return;
  }
  if (user.rules.length === 0) {
    await ctx.reply('📋 No active rules. Create some in the GuardAgent app!');
    return;
  }

  const list = user.rules.map((r: { name: string; tokenSymbol: string; condition: string; threshold: number }, i: number) => {
    const cond = r.condition === 'ABOVE' ? '▲' : '▼';
    return `${i + 1}. *${r.name}*\n   ${r.tokenSymbol} ${cond} $${r.threshold.toLocaleString()}`;
  }).join('\n\n');

  await ctx.reply(`📋 *Your Active Rules:*\n\n${list}`, { parse_mode: 'Markdown' });
});

