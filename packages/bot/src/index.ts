const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:3001';

async function executePendingTx(txId: string): Promise<{ success: boolean; txHash?: string; amount?: string; toAddress?: string; network?: string; error?: string }> {
  const BOT_SHARED_SECRET = process.env.BOT_SHARED_SECRET;
  if (!BOT_SHARED_SECRET) return { success: false, error: 'BOT_SHARED_SECRET not configured' };
  try {
    const res = await fetch(`${BACKEND_URL}/api/agent/pending-tx/${txId}/execute`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'X-Bot-Secret': BOT_SHARED_SECRET },
    });
    const data = await res.json() as any;
    if (!res.ok) return { success: false, error: data.error || 'Execution failed' };
    return { success: true, txHash: data.txHash, amount: data.amount, toAddress: data.toAddress, network: data.network };
  } catch (err) {
    return { success: false, error: err instanceof Error ? err.message : 'Unknown error' };
  }
}

async function rejectPendingTx(txId: string): Promise<{ success: boolean; error?: string }> {
  const BOT_SHARED_SECRET = process.env.BOT_SHARED_SECRET;
  if (!BOT_SHARED_SECRET) return { success: false, error: 'BOT_SHARED_SECRET not configured' };
  try {
    const res = await fetch(`${BACKEND_URL}/api/agent/pending-tx/${txId}/reject`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'X-Bot-Secret': BOT_SHARED_SECRET },
    });
    const data = await res.json() as any;
    if (!res.ok) return { success: false, error: data.error || 'Rejection failed' };
    return { success: true };
  } catch (err) {
    return { success: false, error: err instanceof Error ? err.message : 'Unknown error' };
  }
}

async function executeSwapForAlert(alertId: string): Promise<{ success: boolean; fromAmount?: string; toAmount?: string; txHash?: string; network?: string; error?: string }> {
  // Two-factor authentication to backend:
  //   (1) BOT_SHARED_SECRET in X-Bot-Secret, proves call originates from this bot process.
  //   (2) per-alert BotSwapGrant looked up by alertId on the backend, generated by the
  //       worker at alert-create time, expires in 30 min, single-use.
  // Replaces the prior model which reused a 7-day web JWT for any user.
  try {
    const BOT_SHARED_SECRET = process.env.BOT_SHARED_SECRET;
    if (!BOT_SHARED_SECRET) {
      return { success: false, error: 'BOT_SHARED_SECRET not configured' };
    }
    const res = await fetch(`${BACKEND_URL}/api/bot-swap/execute`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'X-Bot-Secret': BOT_SHARED_SECRET,
      },
      body: JSON.stringify({ alertId }),
    });
    const data = await res.json() as any;
    if (!res.ok) return { success: false, error: data.error || 'Swap failed' };
    return { success: true, fromAmount: data.fromAmount, toAmount: data.toAmount, txHash: data.txHash, network: data.network };
  } catch (err) {
    return { success: false, error: err instanceof Error ? err.message : 'Unknown error' };
  }
}

