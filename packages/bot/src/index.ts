// --- BullMQ Alert delivery worker ---

const alertWorker = new Worker(
  'alerts',
  async (job) => {
    // Regular text alert with inline buttons
    if (job.name === 'send-alert') {
      const { alertId, chatId, message, ruleId, tokenSymbol, condition, threshold, currentPrice, userId } = job.data as {
        alertId: string;
        chatId: string;
        message: string;
        ruleId?: string;
        tokenSymbol?: string;
        condition?: string;
        threshold?: number;
        currentPrice?: number;
        userId?: string;
      };

      try {
        // Append AI insight if price data is available
        let finalMessage = message;
        if (tokenSymbol && condition && threshold && currentPrice) {
          const insight = await getAiInsight(tokenSymbol, condition, threshold, currentPrice);
          if (insight) {
            finalMessage += `\n\n🤖 <i>${insight}</i>`;
          }
        }

        // Check if user has agent wallet → show Execute button immediately
        const hasAgentWallet = userId
          ? await prisma.agentWallet.findUnique({ where: { userId }, select: { id: true } }).then(w => !!w)
          : false;

        const keyboard = ruleId
          ? Markup.inlineKeyboard([
              ...(hasAgentWallet ? [Markup.button.callback('⚡ Execute', `auto_exec:${alertId}`)] : []),
              Markup.button.callback('✅ Acknowledge', `ack:${alertId}`),
              Markup.button.callback('🔕 Disable Rule', `disable_rule:${ruleId}`),
            ])
          : undefined;

        await bot.telegram.sendMessage(chatId, finalMessage, {
          parse_mode: 'HTML',
          ...keyboard,
        });

        // Update status (ignore if alertId doesn't exist, e.g. test jobs)
        await prisma.alert.update({
          where: { id: alertId },
          data: { status: 'SENT', sentAt: new Date() },
        }).catch((err) => { console.warn('[bot] alert status update failed for', alertId, err); });

        console.log(`Alert ${alertId} sent to ${chatId}`);
      } catch (err) {
        console.error(`Failed to send alert ${alertId}:`, err);
      }
    }

    // Auto-execute job at T+15 (if autoMode ON)
    if (job.name === 'auto-execute') {
      const { alertId, chatId, tokenSymbol, condition, threshold, currentPrice, userId } = job.data as {
        alertId: string; chatId: string; tokenSymbol: string;
        condition: string; threshold: number; currentPrice: number; userId?: string;
      };

      try {
        const conditionLabel = condition === 'ABOVE' ? 'above' : 'below';

        await bot.telegram.sendMessage(
          chatId,
          `⚡ <b>AutoMode: Executing protective swap...</b>\n\n` +
          `<b>${tokenSymbol}</b> ${conditionLabel} $${threshold.toLocaleString()} · Current: <b>$${currentPrice.toLocaleString()}</b>\n` +
          `🤖 Agent is processing...`,
          { parse_mode: 'HTML' }
        );

        // Auto-execute uses the same per-alert grant, no userId-only path.
        let swapResult = { success: false, fromAmount: '', toAmount: '', txHash: '', error: 'No alertId' };
        if (alertId) swapResult = await executeSwapForAlert(alertId) as any;

        await prisma.alert.update({
          where: { id: alertId },
          data: { executedAt: new Date(), status: 'ACKNOWLEDGED' },
        }).catch((err) => { console.warn('[bot] auto-execute alert update failed for', alertId, err); });

        if (swapResult.success) {
          const explorerBase = 'https://testnet.arcscan.app';
          await bot.telegram.sendMessage(
            chatId,
            `✅ <b>Protective swap complete!</b>\n\n` +
            `📤 Sold: <b>${swapResult.fromAmount}</b>\n` +
            `📥 Received: <b>${swapResult.toAmount}</b>\n` +
            `🌐 Network: Arc Testnet\n` +
            `🔗 <a href="${explorerBase}/tx/${swapResult.txHash}">View on Arcscan</a>`,
            { parse_mode: 'HTML' }
          );
        } else {
          await bot.telegram.sendMessage(chatId, `⚠️ AutoMode swap failed: ${swapResult.error}`, { parse_mode: 'HTML' });
        }

        console.log(`Auto-executed alert ${alertId}: ${swapResult.success ? 'OK' : swapResult.error}`);
      } catch (err) {
        console.error(`Auto-execute failed for ${alertId}:`, err);
      }
    }
  },
  { connection: redis }
);

alertWorker.on('failed', (job, err) => {
  console.error(`Alert job ${job?.id} failed:`, err);
});

